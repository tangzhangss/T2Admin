package javabean.dtl;

public class Dtc {
	private double shui;
	private double dian;
	private int yue;

	public int getYue() {
		return yue;
	}

	public void setYue(int yue) {
		this.yue = yue;
	}

	public double getShui() {
		return shui;
	}

	public void setShui(double shui) {
		this.shui = shui;
	}

	public double getDian() {
		return dian;
	}

	public void setDian(double dian) {
		this.dian = dian;
	}
}
